package poc.model;

public class InfoModel {

	
	 private int user_selling;
	 private int user_buying;
	 private int cnic;
	 private int mobile;
	 private int adlistid;
	 
	 
	 
	 
	public int getAdlistid() {
		return adlistid;
	}
	public void setAdlistid(int adlistid) {
		this.adlistid = adlistid;
	}
	public int getUser_selling() {
		return user_selling;
	}
	public void setUser_selling(int user_selling) {
		this.user_selling = user_selling;
	}
	public int getUser_buying() {
		return user_buying;
	}
	public void setUser_buying(int user_buying) {
		this.user_buying = user_buying;
	}
	public int getCnic() {
		return cnic;
	}
	public void setCnic(int cnic) {
		this.cnic = cnic;
	}
	public int getMobile() {
		return mobile;
	}
	public void setMobile(int mobile) {
		this.mobile = mobile;
	}
	
	 
	 
	
}
